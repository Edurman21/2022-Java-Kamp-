
public class conditionals {
	
	
	
	public static void main(String[] args) {
		int num = 24;
		if(num < 20) {
			System.out.println("num is less than 20");
		}else if(num == 20) {
			System.out.println("num is equal to 20");
		}else {
			System.out.println("num is greater than 20");
		}
	}
}
